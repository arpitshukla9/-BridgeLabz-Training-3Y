package JavaClassAndObject.level1;

public class ClassMobile {
    String brand;
    String model;
    double price;

    ClassMobile(String brand, String model, double price) {
        this.brand = brand;
        this.model = model;
        this.price = price;
    }

    void display() {
        System.out.println("Brand: " + brand);
        System.out.println("Model: " + model);
        System.out.println("Price: " + price);
    }
}
